/******************************Public*Routine******************************\
* Initialize
*
* Initialize the Graphics Engine.  This call is made once by USER.
*
* History:
*  Thu 29-Oct-1992 -by- Patrick Haluptzok [patrickh]
* Remove wrappers, unnecesary semaphore use, bogus variables, cleanup.
*
*  10-Aug-1990 -by- Donald Sidoroff [donalds]
* Wrote it.
\**************************************************************************/

LONG CountInit = 1;

extern "C" BOOL Initialize(VOID)
{
    //
    // We only want to initialize once.  We can detect transition to 0.
    //

    if (InterlockedDecrement(&CountInit) != 0)
    {
        return(TRUE);
    }
#if DBG
    pGdiHeap = RtlCreateHeap( HEAP_GROWABLE | HEAP_CLASS_3,    // Flags
                              NULL,             // HeapBase
                              64 * 1024,        // ReserveSize
                              4096,             // CommitSize
                              NULL,             // Lock to use for serialization
                              NULL              // GrowthThreshold
                            );
#else
    pGdiHeap = RtlProcessHeap();
#endif // DBG
    gefDefaultHeightInInches = (LONG) DEFAULT_SCALABLE_FONT_HEIGHT_IN_POINTS;
    gefDefaultHeightInInches /= (LONG) POINTS_PER_INCH;

    //
    // Initialize lots of random stuff including the handle manager.
    //

    DbgPrint("GRE: Initialize — calling HmgCreate\n");
    if (!HmgCreate())
    {
        DbgPrint("GRE: HmgCreate FAILED\n");
        return(FALSE);
    }

    //
    // Initialize REGION time stamp
    //

    REGION::ulUniqueREGION = 1;

    //
    // Initialize LDEV
    //

    DbgPrint("GRE: calling bInitLDEVOBJ\n");
    if (!bInitLDEVOBJ())
    {
        DbgPrint("GRE: bInitLDEVOBJ FAILED\n");
        return(FALSE);
    }

    //
    // Init the font drivers
    //

    DbgPrint("GRE: calling bInitPathAlloc\n");
    if (!bInitPathAlloc())
    {
        DbgPrint("GRE: bInitPathAlloc FAILED\n");
        return(FALSE);
    }

// Create the RFONT list semaphore.

    ghsemRFONTList = hsemCreate();
    ASSERTGDI(ghsemRFONTList != (HSEM) 0, "ghsemRFONTList failed to create");

    //
    // Create a null region as the default region
    //

    hrgnDefault = GreCreateRectRgn(0, 0, 0, 0);

    if (hrgnDefault == (HRGN) 0)
    {
        RIP("hrgnDefault failed to initialize\n");
        return(FALSE);
    }

    {
        RGNOBJAPI ro(hrgnDefault);
        ASSERTGDI(ro.bValid(),"invalid hrgnDefault\n");

        prgnDefault = ro.prgnGet();
    }

    //
    // Create a monochrome 1x1 bitmap as the default bitmap
    //

    DbgPrint("GRE: calling bInitPALOBJ\n");
    if (!bInitPALOBJ())
    {
        DbgPrint("GRE: bInitPALOBJ FAILED\n");
        return(FALSE);
    }

    vInitXLATE();

    DbgPrint("GRE: calling bInitBMOBJ\n");
    if (!bInitBMOBJ())
    {
        DbgPrint("GRE: bInitBMOBJ FAILED\n");
        return(FALSE);
    }

    //
    // Init the font drivers
    //

    DbgPrint("GRE: calling bInitFontDrivers\n");
    if (!bInitFontDrivers())
    {
        DbgPrint("GRE: bInitFontDrivers FAILED\n");
        return(FALSE);
    }

    //
    // Init global public PFT
    //

    DbgPrint("GRE: calling bInitPublicPFT\n");
    if (!bInitPublicPFT())
    {
        DbgPrint("GRE: bInitPublicPFT FAILED\n");
        return(FALSE);
    }

    //
    // Initialize LFONT
    //

    DbgPrint("GRE: calling bInitStockFonts\n");
    if (!bInitStockFonts())
    {
        DbgPrint("GRE: bInitStockFonts FAILED\n");
        return(FALSE);
    }

    DbgPrint("GRE: calling bInitFontCache\n");
    if (!bInitFontCache())
    {
        DbgPrint("GRE: bInitFontCache FAILED\n");
        return(FALSE);
    }

    //
    // Init font substitution table
    //

    vInitFontSubTable();

    //
    // Start up the brush component
    //

    DbgPrint("GRE: calling bInitBRUSHOBJ\n");
    if (!bInitBRUSHOBJ())
    {
        DbgPrint("GRE: bInitBRUSHOBJ FAILED\n");
        return(FALSE);
    }


    //
    // Load default face names for the mapper from the registry
    //

    vInitMapper();


#ifdef FONTLINK
    vInitializeEUDC();
#endif

    DbgPrint("GRE: Initialize() complete\n");
    return(TRUE);
}
